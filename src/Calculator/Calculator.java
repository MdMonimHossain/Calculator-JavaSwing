    private void initVariables(){
        numberBuffer[0] = 0;
        numbers = 0;
        operators = 0;
        operatorSelected = false;
    }
    
    private void displayNumbers(String num){
        if(display.getText().equals("0")){
            display.setText(num);
        }
        else if(operatorSelected){
            display.setText(num);
            operatorSelected = false;
        }
        else if(display.getText().length() < MAX_DIGIT){
            display.setText(display.getText() + num);
        }
    }
    
    private void addToBuffer(String operator){
        double operand = Double.parseDouble(display.getText());
        numberBuffer[numbers] = operand;
        operatorBuffer[operators] = operator;
        numbers++;
        operators++;
        operatorSelected = true;
    }
    
    private void updateBuffer(){
        for(int i = 1; i < numbers - 1; i++){
            numberBuffer[i] = numberBuffer[i + 1];
        }
        for(int i = 0; i < operators - 1; i++){
            operatorBuffer[i] = operatorBuffer[i + 1];
        }
        numbers--;
        operators--;
    }
    
    private void showAnswer(double ans){
        
        String str = String.valueOf(ans);
        
        if(str.length() <= MAX_DIGIT){
            if(str.charAt(str.length() - 1) == '0' && str.charAt(str.length() - 2) == '.'){
                display.setText(str.substring(0, str.length() - 2));
            }
            else{
                display.setText(str);
            }
        }
        else{
            if(str.indexOf('E') == -1){
                display.setText(str.substring(0, MAX_DIGIT));
            }
            else{
                String exponentPart = str.substring(str.indexOf('E'));
                str = str.substring(0, MAX_DIGIT - exponentPart.length());
                str = str + exponentPart;
                
                display.setText(str);
            }
        }
    }
    
    private void performOperation(double operand1, double operand2, String operator){
        
        double ans = 0;
        if(operator.equals("+")){
            ans = operand1 + operand2;
        }
        if(operator.equals("-")){
            ans = operand1 - operand2;
        }
        if(operator.equals("*")){
            ans = operand1 * operand2;
        }
        if(operator.equals("/")){
            ans = operand1 / operand2;
        }
        numberBuffer[0] = ans;
        updateBuffer();
    }
    
    private void performOperation(String operator){
        
        double ans = 0;
        double operand = Double.parseDouble(display.getText());
        
        if(operator.equals("âˆš")){
            ans = Math.sqrt(operand);
        }
        if(operator.equals("%")){
            ans = operand / 100;
        }
        
        showAnswer(ans);
    }
       
    
